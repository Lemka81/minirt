#include "libft.h"

char	*ft_strdup(const char *str)
{
	char		*out;
	size_t		size;

	size = ft_strlen((char *)str);
	out = malloc(++size);
	if (!out)
		return (0);
	ft_strlcpy(out, str, size);
	return (out);
}
