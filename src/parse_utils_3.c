int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	return (s1[i] - s2[i]);
}

void	ft_error(t_store *st, char *msg)
{
	ft_putstr_fd(msg, 2);
	ft_close_red_cross(st);
}

static size_t	ft_intlen(int n)
{
	size_t		len;

	len = 0;
	if (!n)
		len++;
	while (n)
	{
		n = n / 10;
		len++;
	}
	return (len);
}

int	ft_isspace(int c)
{
	return (c == '\t' || c == '\n' || c == '\v'
		|| c == '\f' || c == '\r' || c == ' ');
}

double	ft_atof(char *str)
{
	double	atof;
	int		atoi;
	int		i;
	int		fac;

	fac = 1;
	atof = 0;
	i = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
		fac = -1;
	atoi = ft_atoi(str);
	i = ft_intlen(atoi);
	if (fac == -1)
		i++;
	if (str[i++] != '.')
		return (atoi);
	while (str[i] >= '0' && str[i] <= '9')
	{
		fac *= 10;
		atof = atof * 10 + str[i] - 48;
		i++;
	}
	return (atoi + (atof / fac));
}
